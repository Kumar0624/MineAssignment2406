// increment operator ++

// post increment operator ++

let g = 10
console.log("Value of g after post increment is = ", g++) // here g++ will return the value first and then increment
console.log("Value of g after post increment is = ", g) // here g will have incremented value

// pre increment operator ++

let h = 15
console.log("Value of h after pre increment is = ", ++h) // h = h + 1 here ++h will increment the value first and then return
console.log("Value of h after pre increment is = ", h) // here h will have incremented value

// decrement operator --

// post decrement operator --
let i = 20
console.log("Value of i after post decrement is = ", i--) // here i-- will return the value first and then decrement
console.log("Value of i after post decrement is = ", i) // here i will have decremented value

// pre decrement operator --
let j = 25
console.log("Value of j after pre decrement is = ", --j) // j = j - 1 here --j will decrement the value first and then return
console.log("Value of j after pre decrement is = ", j) // here j will have decremented value