
// arithmetic operators

let a = 2
let b = 2
console.log("Addition of a and b is = ", a + b) // addition operator +
console.log("Subtraction of a and b is = ", a - b) // subtraction operator -
console.log("Multiplication of a and b is = ", a * b) // multiplication operator *
console.log("Division of a and b is = ", a / b) // division operator /
console.log("Modulus of a and b is = ", a % b) // modulus operator %
console.log("Exponentiation of a to the power b is = ", a ** b) // exponentiation operator **   2 to the power 2 = 4
