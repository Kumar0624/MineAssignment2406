
// Add and assign operator +=

let a = 10
a += 5 // a = a + 5 
console.log("Value of a after += 5 is = ", a)

// Subtract and assign operator -=

let b = 20
b -= 5 // b = b - 5
console.log("Value of b after -= 5 is = ", b)

// Multiply and assign operator *=

let c = 5
c *= 4 // c = c * 4
console.log("Value of c after *= 4 is = ", c)

// Divide and assign operator /=
// The divide operator performs division between two numbers and returns the quotient.
let d = 20
d /= 4 // d = d / 4
console.log("Value of d after /= 4 is = ", d)

// Modulus and assign operator %=
// The modulus operator returns the remainder of a division.
let e = 25
e %= 6 // e = e % 6
console.log("Value of e after %= 6 is = ", e)

// Exponentiation and assign operator **=

let f = 2
f **= 3 // f = f ** 3
console.log("Value of f after **= 3 is = ", f)

